namespace DataDesigner.Core.DataMember
{
    /// <summary>
    /// Member for enum code.
    /// </summary>

    public sealed class EnumMember
    {
        /// <summary>
        /// Member name.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Member value.
        /// </summary>
        public int Value { get; set; }

        /// <summary>
        /// Member comment.
        /// </summary>
        public string Comment { get; set; }
    }
}
